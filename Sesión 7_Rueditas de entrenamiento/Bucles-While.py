# Bucle - While: Recorrer determinado número de iteraciones, puede ser indeterminado

'''
while condicion:
'''

contador = 0        # Variable contador

while contador < 5:             # While y la condición
    print("Contador: ", contador)       # Lo que se va a ejecutar mientras el contador aumenta
    contador += 1               # Parámetro que define el ritmo del contador

print ("Fin del ciclo")         # Validación de que el bucle no es infinito



